    private void jbinsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbinsertActionPerformed
        Pembeli pembeli = new Pembeli();
        try {
            pembeli.setId_Pembeli(Integer.parseInt(this.jtidpembeli.getText()));
            pembeli.setNama_Pembeli(this.jtnama_pembeli.getText());
            pembeli.setNo_telp(this.jtno_telp.getText());
            
            
            this.pembeli.getDataPembeli();
            this.pembeli.insertPembeli(pembeli);
            
            this.ShowTablePembeli();
        } catch (SQLException  err) {
            System.out.println(err);
        }
    }//GEN-LAST:event_jbinsertActionPerformed

    private void jtbldata_pembeliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbldata_pembeliMouseClicked
        int baris = jtbldata_pembeli.getSelectedRow();
        this.jtidpembeli.setText(jtbldata_pembeli.getValueAt(baris, 0 ).toString());
        this.jtnama_pembeli.setText(jtbldata_pembeli.getValueAt(baris, 1 ).toString());
        this.jtno_telp.setText(jtbldata_pembeli.getValueAt(baris, 2).toString());
    }//GEN-LAST:event_jtbldata_pembeliMouseClicked

