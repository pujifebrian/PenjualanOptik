    private void ShowTablePembeli() throws SQLException {
        DefaultTableModel dtmPembeli = new DefaultTableModel(new String[]
        {"ID Pembeli","Nama Pembeli","No Telp"},0);
        dtmPembeli.setRowCount(0); 
        
        for(Pembeli dp : this.pembeli.getDataPembeli()){
            dtmPembeli.addRow(new String[]{dp.getId_Pembeli().toString(), dp.getNama_Pembeli().toString(), dp.getNo_telp().toString()});
        }

        this.jtbldata_pembeli.setModel(dtmPembeli);
    }
    
    private void showComboBoxFilm() throws SQLException {
        DefaultComboBoxModel dcbmProduk = new DefaultComboBoxModel();

        for (Produk f : this.produk.getDataProduk()) {
            dcbmProduk.addElement(f.getJenis());
        }
        this.jcjenis_produk.setModel(dcbmProduk);
    }
